#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*ptr;
	t_list	*tmp;

	ptr = *lst;
	while (ptr)
	{
		(*del)(ptr->content);
		tmp = ptr->next;
		free(ptr);
		ptr = tmp;
	}
	*lst = 0;
}
